USE DbProgCapasEntityFramework;

CREATE TABLE Producto(
	ID_PRODUCTO INT IDENTITY(1,1),
	DESCRIPCION VARCHAR(200) UNIQUE,
	PRECIO DECIMAL(8,2),
	CONSTRAINT PK_PRODUCTO PRIMARY KEY (ID_PRODUCTO)
);

CREATE TABLE Pedido(
	ID_PEDIDO INT IDENTITY(1,1),
	DNI_CLIENTE BIGINT,
	FECHA DATETIME,
	CONSTRAINT PK_PEDIDO PRIMARY KEY(ID_PEDIDO)
);

CREATE TABLE Producto_Pedido(
	ID_PEDIDO INT,
	ID_PRODUCTO INT,
	CANTIDAD INT,
	SUBTOTAL DECIMAL(8,2),
	CONSTRAINT PK_PRODUCTO_PEDIDO PRIMARY KEY(ID_PEDIDO,ID_PRODUCTO),
	CONSTRAINT FK_PRODUCTO_PEDIDO_PEDIDO FOREIGN KEY(ID_PEDIDO) REFERENCES Pedido(ID_PEDIDO),
	CONSTRAINT FK_PRODUCTO_PEDIDO_PRODUCTO FOREIGN KEY(ID_PRODUCTO) REFERENCES Producto(ID_PRODUCTO)
);


GO

